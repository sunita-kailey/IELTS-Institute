<h1>Frequently asked questions about IELTS</h1>
<div class="flex">

<h3>1. Where can I take IELTS?</h3>
<p>The network of IELTS test centres managed by IDP: IELTS Australia offers you more than 200 test locations in 45 countries.</p>

<h3>2. How much does it cost?</h3>
<p>IELTS test fees vary from country to country. Your IDP Education student counsellor will advise the test fee for your country in your local currency, or contact your nearest test centre. The IELTS test fee is the same for both versions of the test (Academic and General Training).
</p>
<h3>3. How do I book/register for the test?</h3>
<p>Your IDP Education counsellor can assist you in choosing a test date and location.</p>

<h3>4. What if I need to postpone or cancel my IELTS test?</h3>
<p>Please contact the test centre where you booked your IELTS test. If you postpone or cancel your application more than 5 weeks before the test date, you will receive a refund minus an administration charge. If you postpone or cancel within 5 weeks of the test date, you will be charged the full fee unless you have a medical reason. If you are unable to attend due to a serious medical condition and provide a medical certificate within 5 days of the test date, you will receive a refund minus the local administrative cost. If you are delayed by circumstances beyond your control (e.g. a transport strike) the test centre may offer you a test on the next available test date.
</p>
<h3>5. Do I take all parts of the test on the same day? Which part do I take first?</h3>
<p>The Listening, Reading and Writing components of the test are always completed immediately after each other and with no break. You will take the Speaking test up to 7 days either before or after the test date, depending on the test centre. Many of our test centres will allow you to choose if you wish to complete all four components of the test on test day or if you wish to complete the Speaking component on a different day, depending on what suits you best. You will do the Listening component first followed by the Reading and Writing components of the test.
</p>
<h3>6. What are the test rules?</h3>
<p>Test rules and guidelines can be found on the Notice to Candidates provided in the IELTS Application Form. You are also advised to read the Information for Candidates booklet carefully so that you understand the test format and know what to expect on test day. IDP Education offers practical advice for test day on www.ieltsessentials.com.
</p>
<h3>7. What can I take into the examination room?</h3>
<p>Only pens, pencils and erasers. You must bring the passport/national identity card you used on the IELTS Application Form to the test. You must leave everything else outside the examination room. Mobile phones and pagers must be switched off and placed with personal belongings in the area designated by the supervisor. If you do not switch off your phone/pager or if you keep it with you, you will be disqualified.
</p>
<h3>8. What is the Speaking test?</h3>
<p>The Speaking test is a discussion over 11-14 minutes with a certified and highly qualified IELTS Examiner. It is made up of three sections and is recorded.
</p>
<h3>9. What do I need for the Speaking test?</h3>
<p>You must bring the same passport/national identity card you supplied on your IELTS Application Form and continue to use the same identity documents for each part of the test. Your identity will be checked before you enter the interview room.

<h3>10. What kinds of accents can be heard in the Listening and Speaking tests?</h3>
<p>As IELTS is an international test, a variety of English accents is used in both of these tests.</p>

<h3>11. Can I write in capital letters?</h3>
<p>Yes, you can use all capital letters in the IELTS Reading and Listening components and also in the Writing component.</p>

<h3>12. How are the tests marked?</h3>
<p>IELTS uses a 9-band scoring system to report test scores in a consistent manner. You receive individual band scores for Listening, Reading, Writing and Speaking and an Overall Band Score on a band scale from one to nine.
</p>
<h3>13. Can I make notes on the Listening and Reading question papers?</h3>
<p>Yes. The people marking your answers to the Listening and Reading questions will not see your question paper.</p>

<h3>14. Do I have to write in pencil? (Paper-based IELTS only)</h3>
<p>Using a pencil is recommended for the IELTS Listening and Reading of the test.</p>

<p>In many test centres, we now also require the Writing test to be completed in pencil. This is because gradually, all of our test centres are upgrading to new scanners. These particular scanners work best with a pencil as there is no risk of your answers smudging (whereas there is with pen/ink). These test centres are also using answers sheets with a slightly updated design.

No changes have been made to the test format, content, or marking standards. This is simply an operational change. It is also important to note that if you forget to bring a pencil, the test centre staff can provide a pencil for you.
</p>
<h3>15. Who sets the 'pass mark' for the IELTS test?</h3>
<p>There is no pass or fail in IELTS. Scores are graded on the 9-band system. Each educational institution or organisation sets its own minimum IELTS scores to meet its individual requirements.
</p>
